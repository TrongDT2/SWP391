package model;

/**
 *
 * @author Aur
 */
public class Article {
     private int id;
  private String nameCategory, content, image, date,title,author;

    public Article() {
    }

    public Article(int id, String nameCategory, String content, String image, String date, String title, String author) {
        this.id = id;
        this.nameCategory = nameCategory;
        this.content = content;
        this.image = image;
        this.date = date;
        this.title = title;
        this.author = author;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNameCategory() {
        return nameCategory;
    }

    public void setNameCategory(String nameCategory) {
        this.nameCategory = nameCategory;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
